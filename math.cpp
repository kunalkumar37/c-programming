#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    int x=4,y=5;
    cout<<"the greater number is ::"<<fmax(x,y)<<endl;
    cout<<"the smaller number ::"<<fmin(x,y)<<endl;
    cout<<"the difference ::"<<fdim(y,x)<<endl;
    cout<<"2 to the power 4::"<<pow(2,4)<<endl;
    cout<<sqrt(264)<<endl;
    cout<<round(6.45)<<endl;
    cout<<round(3.455)<<endl;
    cout<<floor(4.9)<<endl;
    cout<<log(2)<<endl;
    cout<<sin(30)<<endl;

    return 0;

}
